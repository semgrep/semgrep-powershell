and trans_sequence_statement ((kind, body) : mt) : CST.sequence_statement =
  match body with
  | Children v ->
      (match v with
      | Seq [v0; v1] ->
          (
            trans_pat_seq (Run.matcher_token v0),
            trans_statement_block (Run.matcher_token v1)
          )
      | _ -> assert false
      )
  | Leaf _ -> assert false

and trans_statement ((kind, body) : mt) : CST.statement =
  match body with
  | Children v ->
      (match v with
      | Alt (0, v) ->
          `Choice_if_stmt (
            (match v with
            | Alt (0, v) ->
                `If_stmt (
                  trans_if_statement (Run.matcher_token v)
                )
            | Alt (1, v) ->
                `Opt_label_labe_stmt (
                  (match v with
                  | Seq [v0; v1] ->
                      (
                        Run.opt
                          (fun v -> trans_label (Run.matcher_token v))
                          v0
                        ,
                        trans_labeled_statement (Run.matcher_token v1)
                      )
                  | _ -> assert false
                  )
                )
            | Alt (2, v) ->
                `Func_stmt (
                  trans_function_statement (Run.matcher_token v)
                )
            | Alt (3, v) ->
                `Class_stmt (
                  trans_class_statement (Run.matcher_token v)
                )
            | Alt (4, v) ->
                `Enum_stmt (
                  trans_enum_statement (Run.matcher_token v)
                )
            | Alt (5, v) ->
                `Flow_cont_stmt_stmt_term (
                  (match v with
                  | Seq [v0; v1] ->
                      (
                        trans_flow_control_statement (Run.matcher_token v0),
                        trans_statement_terminator (Run.matcher_token v1)
                      )
                  | _ -> assert false
                  )
                )
            | Alt (6, v) ->
                `Trap_stmt (
                  trans_trap_statement (Run.matcher_token v)
                )
            | Alt (7, v) ->
                `Try_stmt (
                  trans_try_statement (Run.matcher_token v)
                )
            | Alt (8, v) ->
                `Data_stmt (
                  trans_data_statement (Run.matcher_token v)
                )
            | Alt (9, v) ->
                `Inli_stmt (
                  trans_inlinescript_statement (Run.matcher_token v)
                )
            | Alt (10, v) ->
                `Para_stmt (
                  trans_parallel_statement (Run.matcher_token v)
                )
            | Alt (11, v) ->
                `Seq_stmt (
                  trans_sequence_statement (Run.matcher_token v)
                )
            | Alt (12, v) ->
                `Pipe_stmt_term (
                  (match v with
                  | Seq [v0; v1] ->
                      (
                        trans_pipeline (Run.matcher_token v0),
                        trans_statement_terminator (Run.matcher_token v1)
                      )
                  | _ -> assert false
                  )
                )
            | Alt (13, v) ->
                `Empty_stmt (
                  trans_empty_statement (Run.matcher_token v)
                )
            | _ -> assert false
            )
          )
      | Alt (1, v) ->
          `Semg_ellips (
            trans_semgrep_ellipsis (Run.matcher_token v)
          )
      | _ -> assert false
      )
  | Leaf _ -> assert false

and trans_statement_block ((kind, body) : mt) : CST.statement_block =
  match body with
  | Children v ->
      (match v with
      | Seq [v0; v1; v2] ->
